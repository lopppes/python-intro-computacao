# Tipos de números em Python:

# números inteiros (int): São números inteiros, positivos ou negativos, sem parte fracionária.

numero_inteiro = 10

print("Número inteiro:", numero_inteiro)  # Número inteiro: 10

# Números de ponto flutuante (float): São números com parte fracionária, representados com um ponto decimal.

numero_flutuante = 3.14

print("Número de ponto flutuante:", numero_flutuante)  # Número de ponto flutuante: 3.14

# Números complexos (complex): São números que têm uma parte real e uma parte imaginária.

numero_complexo = 2 + 3j

print("Número complexo:", numero_complexo)  # Número complexo: (2+3j)

# Operações básicas com números:

# Adição

soma = 10 + 5

print("Soma:", soma)  # Soma: 15

# Subtração

subtracao = 10 - 5

print("Subtração:", subtracao)  # Subtração: 5

# Multiplicação

multiplicacao = 10 * 5

print("Multiplicação:", multiplicacao)  # Multiplicação: 50

# Divisão

divisao = 10 / 5

print("Divisão:", divisao)  # Divisão: 2.0

# Exponenciação

exponenciacao = 2 ** 3

print("Exponenciação:", exponenciacao)  # Exponenciação: 8

# Divisão inteira

divisao_inteira = 10 // 3

print("Divisão inteira:", divisao_inteira)  # Divisão inteira: 3

# Resto da divisão

resto_divisao = 10 % 3

print("Resto da divisão:", resto_divisao)  # Resto da divisão: 1

# Operações de comparação:

# Igualdade
print("10 é igual a 5?", 10 == 5)  # 10 é igual a 5? False

# Diferença
print("10 é diferente de 5?", 10 != 5)  # 10 é diferente de 5? True

# Maior que
print("10 é maior que 5?", 10 > 5)  # 10 é maior que 5? True

# Menor que
print("10 é menor que 5?", 10 < 5)  # 10 é menor que 5? False

# Maior ou igual que
print("10 é maior ou igual a 5?", 10 >= 5)  # 10 é maior ou igual a 5? True

# Menor ou igual que
print("10 é menor ou igual a 5?", 10 <= 5)  # 10 é menor ou igual a 5? False

# Exemplos mais complexos:

# Expressão com múltiplas operações
expressao = (5 * 2) + (10 / 2) - (3 ** 2)
print("Resultado da expressão:", expressao)  # Resultado da expressão: 1.0

# Usando variáveis em expressões
a = 10
b = 5
resultado = (a * 2) + (b / 2)
print("Resultado usando variáveis:", resultado)  # Resultado usando variáveis: 20.5

# Expressão com comparação
resultado_comparacao = (a * 2) > (b + 5)
print("Resultado da comparação:", resultado_comparacao)  # Resultado da comparação: True
